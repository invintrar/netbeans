    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        comprobar = true;
        int[][] matriz = new int[4][3];

        int yc;
        theta = Double.parseDouble(jTextField3.getText());
        nIteraciones = Integer.parseInt(jTextField7.getText());
        w1 = Double.parseDouble(jTextField1.getText());
        w2 = Double.parseDouble(jTextField2.getText());
        //w1 = Integer.parseInt(jTextField1.getText());
        //w2 = Integer.parseInt(jTextField2.getText());
        //Lee tabla
        for (int i = 0; i < 4; i++) {
            for (int j = 0; j < 3; j++) {
                String valor = jTable1.getValueAt(i, j).toString();
                matriz[i][j] = Integer.parseInt(valor);
            }
        }//fin lectura tabla
        w[0] = w1;
        w[1] = w2;
        //Repite el un numeor de iteraciones
        for (int i = 0; i < nIteraciones; i++) {
            //Recorre la filas j
            for (int j = 0; j < 4; j++) {
                //for sumatoria
                s = 0;
                for (int k = 0; k < 2; k++) {
                    s += matriz[j][k] * w[k];
                }
                //Comprueba funcion
                s = s + theta;
                if (s > 0) {
                    yc = 1;
                    if (yc != matriz[j][2]) {
                        theta = theta - 1;
                        //Calculamos pesos nuevos
                        for (int m = 0; m < 2; m++) {
                            w[m] = w[m] - matriz[j][m];
                        }//For calcula los nuevos pesos
                    }

                } else {
                    yc = -1;
                    if (yc != matriz[j][2]) {
                        theta = theta + 1;
                        //Calculamos pesos nuevos
                        for (int m = 0; m < 2; m++) {
                            w[m] = w[m] + matriz[j][m];
                        }//For calcula los nuevos pesos
                    }
                }
            }//Fin recorre las filas
        }//Fin iteraciones
        String p1=String.valueOf(w[0]);
        String p2=String.valueOf(w[1]);
        jTextField1.setText(p1);
        jTextField2.setText(p2);
        String teta=String.valueOf(theta);
        jTextField3.setText(teta);

    }//GEN-LAST:event_jButton1ActionPerformed

